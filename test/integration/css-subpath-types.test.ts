/**
 * Verifies that CSS subpath exports carry a `types` condition and that the
 * referenced .d.ts shims exist on disk and are valid side-effect module
 * declarations.
 *
 * Regression guard for TS 5.6+ `noUncheckedSideEffectImports`:
 * consumers doing `import "@lgtm-hq/turbo-themes/css"` under strict TS
 * configs must be able to resolve a type declaration.
 */
import { readFileSync, readdirSync, existsSync } from 'fs';
import { dirname } from 'path';
import { describe, it, expect } from 'vitest';

type ExportsValue = string | { types?: string; default?: string; import?: string };
type ExportsMap = Record<string, ExportsValue>;

const pkg = JSON.parse(readFileSync('package.json', 'utf-8')) as {
  exports: ExportsMap;
};

/**
 * Subpath keys in the exports map that map to CSS files and therefore need
 * a `types` condition with a .d.ts shim.
 */
const CSS_SUBPATH_KEYS = Object.entries(pkg.exports)
  .filter(([key, value]) => {
    // Only keys under ./css* or ./adapters/*.css
    const isCssSubpath = key.startsWith('./css') || key.endsWith('.css');
    if (!isCssSubpath) return false;
    // The target resolves to a .css file (direct or wildcard)
    if (typeof value === 'string') return value.endsWith('.css') || value.includes('/*');
    const target = value.default ?? value.import;
    return target ? target.endsWith('.css') || target.includes('/*') : false;
  })
  .map(([key]) => key);

describe('CSS subpath exports have `types` conditions', () => {
  it('discovers CSS subpath exports', () => {
    expect(CSS_SUBPATH_KEYS.length).toBeGreaterThan(0);
  });

  for (const key of CSS_SUBPATH_KEYS) {
    it(`${key} exposes a types condition`, () => {
      const value = pkg.exports[key];
      expect(typeof value, `${key} must be a conditional exports object`).toBe('object');
      expect((value as { types?: string }).types, `${key} must have a types field`).toBeDefined();
    });
  }
});

describe('CSS subpath `types` targets resolve to shim files', () => {
  for (const key of CSS_SUBPATH_KEYS) {
    const value = pkg.exports[key];
    if (typeof value !== 'object' || !value.types) continue;

    const typesPath = value.types.replace(/^\.\//, '');
    const isWildcard = typesPath.includes('*');

    if (!isWildcard) {
      it(`${key} → ${typesPath} exists`, () => {
        expect(existsSync(typesPath), `${typesPath} should be generated by scripts/generate-css-dts.mjs`).toBe(true);
      });

      it(`${key} shim is a valid side-effect module`, () => {
        const content = readFileSync(typesPath, 'utf-8');
        expect(content, `${typesPath} must contain an empty export statement`).toMatch(/export\s*\{\s*\}\s*;/);
      });
    } else {
      // Wildcard: substitute `*` in typesPath with each CSS filename (reproducing
      // Node's exports wildcard resolution) and assert the resolved shim exists.
      // This catches malformed `types` patterns, not just missing shims.
      const defaultTarget = (value.default ?? '').replace(/^\.\//, '');
      const dir = dirname(defaultTarget);
      it(`${key} → ${typesPath} resolves to a shim for every CSS file`, () => {
        expect(existsSync(dir), `${dir} should exist`).toBe(true);
        const cssFiles = readdirSync(dir).filter((f) => f.endsWith('.css'));
        expect(cssFiles.length).toBeGreaterThan(0);
        for (const cssFile of cssFiles) {
          // Node's exports spec: the `*` captured by the key pattern is
          // substituted verbatim into the target. For `./css/components/*`
          // (no extension) the capture is the full basename.
          const resolvedTypes = typesPath.replace(/\*/g, cssFile);
          expect(existsSync(resolvedTypes), `shim not found at ${resolvedTypes}`).toBe(true);
          const content = readFileSync(resolvedTypes, 'utf-8');
          expect(content, `${resolvedTypes} must contain an empty export statement`).toMatch(/export\s*\{\s*\}\s*;/);
        }
      });
    }
  }
});
